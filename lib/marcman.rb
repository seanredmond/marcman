  def self.formatRepeat(r)
    if r == false
      return " (non-repeatable)"
    end

    return " (repeatable)"
  end
  
  def self.formatIndicators(code, ind)
    label = ind == 1 ? "first" : "second"
    indicator = CODES[code][:indicators][ind - 1]
    if indicator == :undefined
      return ["  %s indicator is undefined" % label]
    end

    return ["  %s indicator: %s" % [label, indicator[:definition]]] +
           indicator[:values].map{|k, v| "    %s - %s" % [k, v]}
  end

  def self.addLinkage(code)
    linkage = (CODES[code][:linkage] != false)
    fieldlink = (CODES[code][:fieldlink] != false)

    if linkage and fieldlink
      return CODES[code][:subfields].merge(LINKAGE).merge(FIELDLINK)
    end

    if fieldlink
      return CODES[code][:subfields].merge(FIELDLINK)
    end

    if linkage
      return CODES[code][:subfields].merge(LINKAGE)
    end

    return CODES[code][:subfields]
  end

  def self.formatSubfields(code)
    sf = addLinkage(code)
    sf.map{|k, v| "    %s - %s%s" % [k, v[:definition],
                                     formatRepeat(v[:repeat])]}
  end
    
